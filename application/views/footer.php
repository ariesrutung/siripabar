<script>
    // Mendapatkan URL halaman saat ini
    var currentURL = window.location.href;

    // Daftar elemen <a> dalam menu
    var menuLinks = document.querySelectorAll('.navbar-nav a.nav-link');

    // Iterasi melalui setiap tautan menu
    menuLinks.forEach(function(link) {
        // Memeriksa apakah URL tautan cocok dengan URL halaman saat ini
        if (link.href === currentURL) {
            // Menambahkan kelas "active" ke elemen <li> yang mengandung tautan tersebut
            link.parentElement.classList.add('active');
        } else {
            link.parentElement.classList.remove('active'); // Hapus kelas "active" dari elemen <li>
        }
    });

    // Menambahkan kelas "active" pada menu "BERITA" jika Anda berada di halaman berita atau detail_berita
    if (currentURL.includes("berita")) {
        document.querySelector('a[href*="berita"]').parentElement.classList.add('active');
    }

    if (currentURL.includes("galeri")) {
        document.querySelector('a[href*="galeri"]').parentElement.classList.add('active');
    }

    if (currentURL.includes("profil")) {
        document.querySelector('a[href*="profil"]').parentElement.classList.add('active');
    }
</script>

